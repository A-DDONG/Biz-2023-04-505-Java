package com.callor.controller.var;

public class VarC {

	public static void main(String[] args) {
		String str = "ABC";
		System.out.println(str);
		str = "Korea";
		System.out.println(str);
		str = "대한민국";
		System.out.println(str);
		str = "우리나라";
		System.out.println(str);
		// 3개의 문자열을 연결하여 str 변수에 저장
		str = "Republic" + "of" + "Korea";
		System.out.println(str);

	}
}
